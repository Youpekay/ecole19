int		ft_str_is_alpha(char *str)
{
	int		i;
	char	curr_c;

	i = 0;
	while (str[i] != '\0')
	{
		curr_c = str[i];
		if (!((curr_c >= 'A' && curr_c <= 'Z') || (curr_c >= 'a' && curr_c <= 'z')))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
