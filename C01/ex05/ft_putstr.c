#include <unistd.h>

void	 ft_putchr(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	char c;

	while (*str != '\0')
	{
		c = *str;
		ft_putchr(c);
		str++;
	}
}
