int		ft_sqrt(int nb)
{
	unsigned int i;
	unsigned int n;

	n = nb;
	i = 1;
	if (nb < 0)
		return (0);
	while (i * i <= n)
	{
		if ((i * i) == n)
			break ;
		i++;
	}
	if (i * i != n)
		return (0);
	else
		return (i);
}
