#include <unistd.h>

void	ft_putnbr(int nb)
{
	long int a;
    char c;

	a = nb;
	if (a < 0)
	{
		write(1, "-", 1);
		a = -a;
	}
	if (a >= 10)
	{
		ft_putnbr(a / 10);
	}
    c = a % 10 + '0';
	write(1, &c, 1);
}
